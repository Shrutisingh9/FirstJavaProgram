public class loops {
    public static void main(String[] args) {
        System.out.println(1);
        System.out.println(2);
        System.out.println(3);


        System.out.println("Using Loops");
        int i = 1;
        while(i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("Finish Running While Loop!");

    }
}
